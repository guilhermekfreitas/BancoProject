    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        boolean valido = true;
        if(tfConta.toString().equals("")||tfConta.toString().equals(" ")){
            JOptionPane.showMessageDialog(null, "Campo Conta em branco.", "Erro!", JOptionPane.OK_OPTION);
            valido = false;
        }
        if (valido = true){
            String msg = "7 "+tfConta.toString();
            CliThread consulta = new CliThread();
            String retorno = consulta.comunicaServidor(msg, "Administrador");
            int tam = retorno.length();
            char palavra[]=retorno.toCharArray();
            int cont = 2;
            int inicio = 0;
            for (int i = 1; i<=7 ;i++){
                inicio = cont;
                while (palavra[cont]!= ' '){
                    cont++;
                    if (cont == tam){
                        break;
                    }
                }
                switch(i){
                    case 1:{lbNome2.setText(retorno.substring(inicio, cont-1));
                            cont++;
                           }
                    case 2:{lbCpf2.setText(retorno.substring(inicio, cont-1));
                           cont++;
                          }
                    case 3:{lbRg2.setText(retorno.substring(inicio,cont-1));
                            cont++;
                          }
                    case 4:{lbDtNasc2.setText(retorno.substring(inicio, cont-1));
                           }
                    case 5:{lbLogin2.setText(retorno.substring(inicio, cont-1));
                           }
                    case 6:{lbSenha2.setText(retorno.substring(inicio, cont-1));
                            cont++;
                           }
                    case 7:{lbSaldo2.setText(retorno.substring(inicio));}
                }
            }
        }
    }//GEN-LAST:event_btnBuscarActionPerformed


